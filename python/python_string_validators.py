s = input()
for k in s:
        if k.isalnum():
                print(k.isalnum())
                break
else :
        print(False)
for k in s:
        if k.isalpha():
                print(k.isalpha())
                break
else :
        print(False)
for k in s:
        if k.isdigit():
                print(k.isdigit())
                break
else :
        print(False)
for k in s:
        if k.islower():
                print(k.islower())
                break
else :
        print(False)
for k in s:
        if k.isupper():
                print(k.isupper())
                break
else :
        print(False)
